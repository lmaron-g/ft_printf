#include "ft_printf.h"

int	is_flag(char c)
{
	if (c == '-' || c == '+' || c == ' ' || c == '#' || c == '0')
		return (1);
	return (0);
}

int	is_type(char c)
{
	if (c == 'd' || c == 'i' || c == 'u' || c == 'o' || c == 'x' \
		|| c == 'X' || c == 'f' || c == 'c' || c == 's' || c == 'p' || c == '%')
		return (1);
	return (0);
}

int	is_right(char c)
{
	if (c == 'd' || c == 'i' || c == 'u' || c == 'o' || c == 'x'           \
	|| c == 'X' || c == 'f' || c == 'c' || c == 's' || c == 'p' || c == '%' \
	|| c == '-' || c == '+' || c == ' ' || c == '#' || c == '0' || c == 'h'  \
	|| c == 'l' || c == 'L' || c == '*' || c == '.' || ('0' <= c && c <= '9'))
		return (1);
	return (0);
}

int	is_nan(double f)
{
	if (f != f)
		return (1);
	return (0);
}

int	is_inf(double f)
{
	if (f >= 1 / 0.0 && f <= 1 / 0.0)
		return (1);
	return (0);
}
