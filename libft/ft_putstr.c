#include <unistd.h>
#include "libft.h"

void	ft_putstr(char *src)
{
	int i;

	i = 0;
	if (src)
	{
		while (src[i] != '\0')
			i++;
		write(1, src, i);
		g_r += i;
		ft_strdel(&src);
	}
}
